package com.addition;

public class Variables {
	
	public static void main(String[] args) {
		
		
		int age =20;
		
		System.out.println(age);
		
		float a=125.01f;
		
		System.out.println(a);
		
		double price =120.87;
		
		System.out.println(price);
		
		char gender = 'm';
		System.out.println(gender);
		
		boolean pass=true;
		
		System.out.println(pass);
		
	}

}
