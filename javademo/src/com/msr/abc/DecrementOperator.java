package com.msr.abc;

public class DecrementOperator {

	public static void main(String[] args) {
		
		
		int a=5,b,c,d;
		
		
		b= --a;// a=4 b=4
		
		c= b--; // c=4 b=3
		
		d = --c; // c=3 d=3 
		
		
		System.out.println(a);
		
		System.out.println(b);
		
		
		System.out.println(c);
		
		
		System.out.println(d);
	}
}
